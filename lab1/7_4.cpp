#include <iostream>
using namespace std;

// Тэгш өнцөгт хэлбэртэй хавтанг усан дотор байрлуулахад түүний талбай дээр
// хичнээн хэмжээний даралт үүсэх вэ? Усны гүнийг гараас авна

int main() {
	// Шингэний даралт жигд үйлчилдэг учир нягт болон өндрөөр тооцож олно.
	float P;
	int i = 1000; // Шингэний нягт
	int h; // Усны гүн
	int g = 10; // татах хүч
	cin >> h;
	P = i *g * h;
	cout <<  "Тэгш өнцөгт хэлбэртэй хавтангын талбай дээр үүсэх даралт: " << P << endl;
	return 0;
}